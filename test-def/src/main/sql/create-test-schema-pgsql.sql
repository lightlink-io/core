CREATE TABLE departments ( 
	DEPARTMENT_ID  	decimal(4,0) NOT NULL,
	DEPARTMENT_NAME	varchar(30) NOT NULL,
	MANAGER_ID     	decimal(6,0) NULL,
	LOCATION_ID    	decimal(4,0) NULL 
